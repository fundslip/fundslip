"use client";

import { CheckCircle, XCircle, Shield, AlertTriangle } from "lucide-react";
import { PdfViewer } from "@/components/shared/pdf-viewer";
import { CopyButton } from "@/components/shared/copy-button";
import type { VerificationResult } from "@/types";
import { useEffect, useState } from "react";

function fmt(n: number): string {
  return n.toLocaleString("en-US", { minimumFractionDigits: 2, maximumFractionDigits: 2 });
}

interface VerifyResultProps {
  result: VerificationResult;
  fingerprint?: string;
}

export function VerifyResult({ result, fingerprint }: VerifyResultProps) {
  const [pdfBlob, setPdfBlob] = useState<Blob | null>(null);

  useEffect(() => {
    if (!result.verified || !result.statementData) return;

    (async () => {
      try {
        const { generatePdfBlob } = await import("@/lib/pdf");
        const blob = await generatePdfBlob(
          result.statementData!,
          fingerprint || "",
          "",
          fingerprint ? `https://fundslip.xyz/verify?p=${encodeURIComponent(fingerprint)}` : ""
        );
        setPdfBlob(blob);
      } catch (e) {
        console.error("PDF preview error:", e);
      }
    })();
  }, [result, fingerprint]);

  // ── FAILURE ──
  if (!result.verified) {
    return (
      <div className="max-w-2xl mx-auto">
        <div className="bg-white rounded-2xl overflow-hidden shadow-sm border-l-4 border-error">
          <div className="p-8 md:p-10">
            <div className="flex items-start gap-4 mb-6">
              <div className="w-12 h-12 bg-error-container rounded-full flex items-center justify-center flex-shrink-0">
                <XCircle className="w-6 h-6 text-error" />
              </div>
              <div>
                <span className="inline-block px-3 py-1 bg-error-container text-on-error-container text-[10px] font-bold rounded-full mb-2 uppercase tracking-wider">
                  Verification Failed
                </span>
                <h3 className="font-headline text-lg font-bold text-on-surface">
                  Unable to Verify Statement
                </h3>
              </div>
            </div>
            <div className="bg-error-container/20 rounded-xl p-5">
              <div className="flex items-start gap-3">
                <AlertTriangle className="w-5 h-5 text-error flex-shrink-0 mt-0.5" />
                <p className="text-sm text-on-surface leading-relaxed">
                  {result.error || "The verification code could not be validated."}
                </p>
              </div>
            </div>
            <div className="mt-6 pt-5 border-t border-surface-container">
              <p className="text-xs text-on-surface-variant leading-relaxed">
                <strong>What this means:</strong> The cryptographic signature does not match the claimed data.
                The document may have been modified, the code entered incorrectly, or the document was not generated by Fundslip.
              </p>
            </div>
          </div>
        </div>
      </div>
    );
  }

  // ── SUCCESS — two-column layout ──
  const truncatedFp = fingerprint
    ? `${fingerprint.slice(0, 12)}...${fingerprint.slice(-12)}`
    : "";

  return (
    <div className="flex flex-col lg:flex-row gap-10 items-start">
      {/* Left Column: Verified details */}
      <div className="w-full lg:w-5/12 space-y-5">
        {/* Verified badge */}
        <div className="bg-white rounded-2xl overflow-hidden shadow-sm border-l-4 border-tertiary-fixed p-6">
          <div className="flex items-center gap-4 mb-4">
            <div className="w-12 h-12 bg-tertiary-fixed/20 rounded-full flex items-center justify-center flex-shrink-0">
              <CheckCircle className="w-6 h-6 text-tertiary" />
            </div>
            <div>
              <span className="inline-block px-3 py-1 bg-tertiary-fixed text-tertiary text-[10px] font-bold rounded-full mb-1 uppercase tracking-wider">
                Verified Authentic
              </span>
              <h3 className="font-headline text-lg font-bold">
                {result.ensName || `${result.walletAddress.slice(0, 8)}...${result.walletAddress.slice(-6)}`}
              </h3>
            </div>
          </div>
          <div className="flex items-center gap-2 text-xs text-on-surface-variant">
            <Shield className="w-3.5 h-3.5" />
            <span>Wallet signature cryptographically verified. This statement is authentic.</span>
          </div>
        </div>

        {/* Account details */}
        <div className="bg-surface-container-lowest rounded-xl p-5 ring-1 ring-outline-variant/15 space-y-4">
          <div>
            <p className="text-[10px] uppercase tracking-widest text-on-surface-variant font-bold mb-1">Wallet Address</p>
            <p className="text-xs font-mono text-on-surface truncate">{result.walletAddress}</p>
          </div>
          <div>
            <p className="text-[10px] uppercase tracking-widest text-on-surface-variant font-bold mb-1">Block Number</p>
            <p className="text-sm font-semibold">#{result.blockNumber?.toLocaleString()}</p>
          </div>
          <div>
            <p className="text-[10px] uppercase tracking-widest text-on-surface-variant font-bold mb-1">Verified Balance</p>
            <p className="text-2xl font-headline font-extrabold text-primary">${fmt(result.totalValueUsd)}</p>
          </div>
        </div>

        {/* Data Fingerprint */}
        {fingerprint && (
          <div className="bg-surface-container-lowest rounded-xl p-5 ring-1 ring-outline-variant/15">
            <p className="text-[10px] uppercase tracking-widest text-on-surface-variant font-bold mb-2">Data Fingerprint</p>
            <div className="flex items-center gap-2">
              <code className="flex-grow bg-surface-container-low px-3 py-2 rounded text-[11px] font-mono text-on-surface-variant truncate">
                {truncatedFp}
              </code>
              <CopyButton text={fingerprint} />
            </div>
          </div>
        )}

        <div>
          <p className="text-[10px] uppercase tracking-widest text-on-surface-variant font-bold mb-1">Verified At</p>
          <p className="text-sm text-on-surface">
            {result.verifiedAt.toLocaleString("en-US", {
              month: "long", day: "numeric", year: "numeric",
              hour: "2-digit", minute: "2-digit", timeZoneName: "short",
            })}
          </p>
        </div>
      </div>

      {/* Right Column: PDF Preview */}
      <div className="w-full lg:w-7/12">
        {pdfBlob ? (
          <PdfViewer pdfBlob={pdfBlob} />
        ) : (
          <div className="flex items-center justify-center py-20 text-on-surface-variant text-sm">
            Generating preview...
          </div>
        )}
      </div>
    </div>
  );
}
