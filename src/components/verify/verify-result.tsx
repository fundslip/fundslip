"use client";

import { CheckCircle, XCircle, AlertTriangle, Copy, Check, ShieldCheck } from "lucide-react";
import { PdfViewer } from "@/components/shared/pdf-viewer";
import type { VerificationResult } from "@/types";
import { useEffect, useState, useCallback } from "react";
import Image from "next/image";
import { copyToClipboard } from "@/lib/clipboard";

function fmt(n: number): string {
  return n.toLocaleString("en-US", { minimumFractionDigits: 2, maximumFractionDigits: 2 });
}

interface VerifyResultProps { result: VerificationResult; fingerprint?: string; }

export function VerifyResult({ result, fingerprint }: VerifyResultProps) {
  const [pdfBlob, setPdfBlob] = useState<Blob | null>(null);
  const [fpCopied, setFpCopied] = useState(false);

  const handleCopyFp = useCallback(async () => {
    if (!fingerprint) return;
    const ok = await copyToClipboard(fingerprint);
    if (ok) { setFpCopied(true); setTimeout(() => setFpCopied(false), 2000); }
  }, [fingerprint]);

  useEffect(() => {
    if (!result.verified || !result.statementData) return;
    (async () => {
      try {
        const { generatePdfBlob } = await import("@/lib/pdf");
        const blob = await generatePdfBlob(result.statementData!, fingerprint || "", "",
          fingerprint ? `https://fundslip.xyz/verify?p=${encodeURIComponent(fingerprint)}` : "");
        setPdfBlob(blob);
      } catch (e) { console.error("PDF preview error:", e); }
    })();
  }, [result, fingerprint]);

  // ── FAILURE ──
  if (!result.verified) {
    return (
      <div className="max-w-xl mx-auto text-center">
        <div className="w-12 h-12 bg-error/5 rounded-full flex items-center justify-center mx-auto mb-5">
          <XCircle className="w-6 h-6 text-error" />
        </div>
        <h2 className="font-headline text-2xl font-semibold text-brand-black mb-2">Verification Failed</h2>
        <p className="text-on-surface-variant text-[15px] mb-6 max-w-sm mx-auto">
          {result.error || "The statement fingerprint could not be validated."}
        </p>
        <div className="rounded-xl bg-surface p-5 text-left flex items-start gap-3">
          <AlertTriangle className="w-4 h-4 text-error flex-shrink-0 mt-0.5" />
          <p className="text-xs text-on-surface-variant leading-relaxed">
            The cryptographic signature does not match the claimed data. The document may have been modified, the code entered incorrectly, or the document was not generated by Fundslip.
          </p>
        </div>
      </div>
    );
  }

  // ── SUCCESS ──
  const truncatedFp = fingerprint ? `${fingerprint.slice(0, 8)}…${fingerprint.slice(-8)}` : "";

  return (
    <div>
      {/* Success header — centered */}
      <div className="text-center mb-10">
        <h1 className="font-headline text-3xl md:text-4xl font-semibold text-brand-black">
          Statement is authentic
        </h1>
      </div>

      {/* Verified statement card — like the hero preview */}
      <div className="max-w-3xl mx-auto mb-8 rounded-2xl border border-outline-variant bg-white overflow-hidden">
        <div className="flex items-center justify-between px-5 md:px-6 py-3 border-b border-outline-variant">
          <div className="flex items-center gap-2">
            <Image src="/fundslip.svg" alt="" width={14} height={18} style={{ height: "auto" }} />
            <span className="font-headline font-semibold text-[13px] text-brand-black">Fundslip</span>
          </div>
          <div className="flex items-center gap-1 text-tertiary">
            <ShieldCheck className="w-3 h-3" />
            <span className="text-[9px] font-medium uppercase tracking-wide">Verified</span>
          </div>
        </div>
        <div className="px-5 md:px-6 py-5">
          <div className="grid grid-cols-2 md:grid-cols-4 gap-4 mb-5">
            <div>
              <p className="text-[9px] uppercase tracking-wide text-on-surface-variant mb-1">Wallet</p>
              <p className="text-[12px] font-mono text-brand-black truncate">{result.walletAddress}</p>
            </div>
            <div>
              <p className="text-[9px] uppercase tracking-wide text-on-surface-variant mb-1">Balance</p>
              <p className="text-[12px] font-semibold text-brand-black">${fmt(result.totalValueUsd)}</p>
            </div>
            <div>
              <p className="text-[9px] uppercase tracking-wide text-on-surface-variant mb-1">Block</p>
              <p className="text-[12px] text-brand-black">#{result.blockNumber?.toLocaleString()}</p>
            </div>
            <div>
              <p className="text-[9px] uppercase tracking-wide text-on-surface-variant mb-1">Verified</p>
              <p className="text-[12px] text-brand-black">
                {result.verifiedAt.toLocaleDateString("en-US", { month: "short", day: "numeric", year: "numeric" })}
              </p>
            </div>
          </div>

          {/* ETH balance row */}
          {result.ethBalance && parseFloat(result.ethBalance) > 0 && (
            <div className="flex items-center justify-between py-2.5 border-t border-outline-variant/50">
              <div className="flex items-center gap-2.5">
                <div className="w-5 h-5 rounded-full bg-[#627EEA]" />
                <span className="text-[12px] font-medium text-brand-black">Ethereum</span>
              </div>
              <span className="text-[12px] text-brand-black tabular-nums">{parseFloat(result.ethBalance).toFixed(4)} ETH</span>
            </div>
          )}
          {result.tokens.slice(0, 5).map((t) => (
            <div key={t.symbol} className="flex items-center justify-between py-2.5 border-t border-outline-variant/30">
              <span className="text-[12px] font-medium text-brand-black">{t.symbol}</span>
              <span className="text-[12px] text-brand-black tabular-nums">{parseFloat(t.balance).toLocaleString("en-US", { maximumFractionDigits: 4 })}</span>
            </div>
          ))}
        </div>
      </div>

      {/* Fingerprint row */}
      {fingerprint && (
        <div className="max-w-3xl mx-auto mb-8 flex flex-col sm:flex-row items-center justify-between gap-3 text-[12px] text-on-surface-variant">
          <div className="flex items-center gap-2">
            <span>Statement Fingerprint:</span>
            <code className="font-mono text-brand-black">{truncatedFp}</code>
            <button onClick={handleCopyFp} className="text-on-surface-variant hover:text-brand-black transition-colors">
              {fpCopied ? <Check className="w-3 h-3 text-tertiary" /> : <Copy className="w-3 h-3" />}
            </button>
          </div>
          <span>
            {result.verifiedAt.toLocaleString("en-US", { month: "long", day: "numeric", year: "numeric", hour: "2-digit", minute: "2-digit", timeZoneName: "short" })}
          </span>
        </div>
      )}

      {/* PDF preview — full width */}
      <div className="max-w-3xl mx-auto">
        {pdfBlob ? <PdfViewer pdfBlob={pdfBlob} /> : (
          <div className="flex items-center justify-center py-16 text-on-surface-variant text-sm">Generating preview…</div>
        )}
      </div>
    </div>
  );
}
