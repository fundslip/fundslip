"use client";

import { CheckCircle, XCircle, Shield, AlertTriangle } from "lucide-react";
import { PdfViewer } from "@/components/shared/pdf-viewer";
import { CopyButton } from "@/components/shared/copy-button";
import type { VerificationResult } from "@/types";
import { useEffect, useState } from "react";

function fmt(n: number): string {
  return n.toLocaleString("en-US", { minimumFractionDigits: 2, maximumFractionDigits: 2 });
}

interface VerifyResultProps { result: VerificationResult; fingerprint?: string; }

export function VerifyResult({ result, fingerprint }: VerifyResultProps) {
  const [pdfBlob, setPdfBlob] = useState<Blob | null>(null);

  useEffect(() => {
    if (!result.verified || !result.statementData) return;
    (async () => {
      try {
        const { generatePdfBlob } = await import("@/lib/pdf");
        const blob = await generatePdfBlob(result.statementData!, fingerprint || "", "",
          fingerprint ? `https://fundslip.xyz/verify?p=${encodeURIComponent(fingerprint)}` : "");
        setPdfBlob(blob);
      } catch (e) { console.error("PDF preview error:", e); }
    })();
  }, [result, fingerprint]);

  // ── FAILURE ──
  if (!result.verified) {
    return (
      <div className="max-w-lg mx-auto">
        <div className="bg-white rounded-2xl p-8 border-l-[3px] border-error">
          <div className="flex items-start gap-4 mb-5">
            <div className="w-10 h-10 bg-error-light rounded-full flex items-center justify-center flex-shrink-0">
              <XCircle className="w-5 h-5 text-error" />
            </div>
            <div>
              <span className="text-[10px] font-semibold uppercase tracking-[0.08em] text-error">Verification Failed</span>
              <h3 className="font-headline text-base font-bold text-gray-900 mt-0.5">Unable to Verify Statement</h3>
            </div>
          </div>
          <div className="bg-error-light/50 rounded-lg p-4 mb-5">
            <div className="flex items-start gap-2.5">
              <AlertTriangle className="w-4 h-4 text-error flex-shrink-0 mt-0.5" />
              <p className="text-sm text-gray-700 leading-relaxed">
                {result.error || "The verification code could not be validated."}
              </p>
            </div>
          </div>
          <p className="text-xs text-gray-400 leading-relaxed">
            The cryptographic signature does not match. The document may have been modified, the code entered incorrectly, or the document was not generated by Fundslip.
          </p>
        </div>
      </div>
    );
  }

  // ── SUCCESS — two columns ──
  const truncatedFp = fingerprint ? `${fingerprint.slice(0, 12)}...${fingerprint.slice(-12)}` : "";

  return (
    <div className="flex flex-col lg:flex-row gap-10 items-start">
      {/* Left */}
      <div className="w-full lg:w-5/12 space-y-5">
        <div className="bg-white rounded-2xl p-6 border-l-[3px] border-emerald">
          <div className="flex items-center gap-4 mb-3">
            <div className="w-10 h-10 bg-emerald-light rounded-full flex items-center justify-center flex-shrink-0">
              <CheckCircle className="w-5 h-5 text-emerald" />
            </div>
            <div>
              <span className="text-[10px] font-semibold uppercase tracking-[0.08em] text-emerald-dark">Verified Authentic</span>
              <h3 className="font-headline text-base font-bold text-gray-900 mt-0.5">
                {result.ensName || `${result.walletAddress.slice(0, 8)}...${result.walletAddress.slice(-6)}`}
              </h3>
            </div>
          </div>
          <div className="flex items-center gap-2 text-xs text-gray-400">
            <Shield className="w-3.5 h-3.5" strokeWidth={1.5} />
            <span>Signature verified against on-chain data. This statement is authentic.</span>
          </div>
        </div>

        <div className="bg-white rounded-xl p-5 space-y-4">
          <div>
            <p className="section-label mb-1">Wallet Address</p>
            <p className="text-xs font-mono text-gray-600 truncate">{result.walletAddress}</p>
          </div>
          <div>
            <p className="section-label mb-1">Block Number</p>
            <p className="text-sm font-semibold text-gray-900">#{result.blockNumber?.toLocaleString()}</p>
          </div>
          <div>
            <p className="section-label mb-1">Verified Balance</p>
            <p className="text-2xl font-headline font-bold text-navy">${fmt(result.totalValueUsd)}</p>
          </div>
        </div>

        {fingerprint && (
          <div className="bg-white rounded-xl p-5">
            <p className="section-label mb-2">Data Fingerprint</p>
            <div className="flex items-center gap-2">
              <code className="flex-grow bg-gray-50 px-3 py-2 rounded-lg text-[11px] font-mono text-gray-500 truncate">{truncatedFp}</code>
              <CopyButton text={fingerprint} />
            </div>
          </div>
        )}

        <p className="text-xs text-gray-400">
          Verified {result.verifiedAt.toLocaleString("en-US", { month: "long", day: "numeric", year: "numeric", hour: "2-digit", minute: "2-digit", timeZoneName: "short" })}
        </p>
      </div>

      {/* Right — PDF */}
      <div className="w-full lg:w-7/12">
        {pdfBlob ? <PdfViewer pdfBlob={pdfBlob} /> : (
          <div className="flex items-center justify-center py-20 text-gray-400 text-sm">Generating preview...</div>
        )}
      </div>
    </div>
  );
}
